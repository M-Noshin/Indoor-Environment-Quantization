#!/usr/bin/env python3
"""Export raw CTF sweeps (Re/Im, pre-normalization) to ctf_recordings.h for e2e firmware."""

import argparse
import os
import sys

import numpy as np

_REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DEFAULT_DATA_DIR = os.path.join(_REPO_ROOT, "training", "data", "indoor_environment")

GLOBAL_MIN = -0.011066
GLOBAL_MAX = 0.011379

MAT_FILES = {
    "CTF_class_mov": "CTF_Class_mov_final.mat",
    "CTF_class_static": "CTF_Class_static_final.mat",
    "CTF_corridor_mov": "CTF_Corridor_mov_final.mat",
    "CTF_corridor_static": "CTF_Corridor_static_final.mat",
    "CTF_lab_mov": "CTF_Lab_mov_final.mat",
    "CTF_lab_static": "CTF_Lab_static_final.mat",
    "CTF_SC_mov": "CTF_SC_mov_final.mat",
    "CTF_SC_static": "CTF_SC_static_final.mat",
}


def center_crop(arr: np.ndarray, alpha: int) -> np.ndarray:
    """arr shape [L, 2] -> [alpha, 2]."""
    length = arr.shape[0]
    if alpha >= length:
        return arr
    start = (length - alpha) // 2
    return arr[start : start + alpha, :]


def load_one_sweep(
    data_dir: str,
    alpha: int,
    index: int = 0,
    mat_key: str = "CTF_class_static",
) -> np.ndarray:
    import scipy.io

    if mat_key not in MAT_FILES:
        raise ValueError(f"Unknown mat_key {mat_key!r}; choose from {sorted(MAT_FILES)}")
    path = os.path.join(data_dir, MAT_FILES[mat_key])
    if not os.path.isfile(path):
        raise FileNotFoundError(f"Missing {path}")

    mat = scipy.io.loadmat(path)
    # Same variable index as training/datasets/indoor_environment_1D.py
    var_keys = [k for k in mat.keys() if not k.startswith("__")]
    key = var_keys[3] if len(var_keys) > 3 else var_keys[0]
    arr = mat[key].T  # [N, 101, 2] or similar
    if arr.ndim == 3:
        sweep = arr[index % arr.shape[0]]
    else:
        sweep = arr
    if sweep.shape[1] != 2 and sweep.shape[0] == 2:
        sweep = sweep.T
    return center_crop(sweep.astype(np.float32), alpha)


def synthetic_sweep(alpha: int) -> np.ndarray:
    """Placeholder sweep in dataset value range when .mat is unavailable."""
    t = np.linspace(-1.0, 1.0, alpha, dtype=np.float32)
    re = GLOBAL_MIN + (t + 1.0) * 0.5 * (GLOBAL_MAX - GLOBAL_MIN)
    im = GLOBAL_MIN + (1.0 - t) * 0.5 * (GLOBAL_MAX - GLOBAL_MIN)
    return np.stack([re, im], axis=1)


def format_recording(name: str, sweep: np.ndarray) -> str:
    alpha = sweep.shape[0]
    lines = [f"static const float {name}[{alpha}][2] = {{"]
    for k in range(alpha):
        re, im = float(sweep[k, 0]), float(sweep[k, 1])
        lines.append(f"  {{ {re:.8f}, {im:.8f} }},")
    lines.append("};")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--data-dir",
        default=DEFAULT_DATA_DIR,
        help="Directory containing CTF_*.mat files (default: training/data/indoor_environment)",
    )
    parser.add_argument("--alpha", type=int, default=91)
    parser.add_argument("--index", type=int, default=0, help="Sweep index in .mat")
    parser.add_argument(
        "--mat-key",
        default="CTF_class_static",
        choices=sorted(MAT_FILES.keys()),
        help="Which .mat bundle to read (first sweep at --index)",
    )
    parser.add_argument(
        "--out",
        default="inference/indoor_env_1d_e2e/ctf_recordings.h",
        help="Output header path",
    )
    parser.add_argument(
        "--synthetic",
        action="store_true",
        help="Write a synthetic sweep (no .mat required)",
    )
    args = parser.parse_args()

    if args.synthetic:
        sweep = synthetic_sweep(args.alpha)
        source_note = "synthetic"
    else:
        sweep = load_one_sweep(args.data_dir, args.alpha, args.index, args.mat_key)
        data_dir_note = os.path.relpath(args.data_dir, _REPO_ROOT)
        if data_dir_note.startswith(".."):
            data_dir_note = args.data_dir
        source_note = f"{args.mat_key} index={args.index} from {data_dir_note}"

    header = f"""// Auto-generated by misc/export_ctf_raw_h.py — raw Re/Im before global min-max
// alpha={args.alpha}, source: {source_note}

#ifndef CTF_RECORDINGS_H
#define CTF_RECORDINGS_H

#define CTF_NUM_RECORDINGS 1
#define CTF_ALPHA {args.alpha}

{format_recording("ctf_recording_0", sweep)}

#endif // CTF_RECORDINGS_H
"""
    os.makedirs(os.path.dirname(os.path.abspath(args.out)), exist_ok=True)
    with open(args.out, "w", encoding="utf-8") as f:
        f.write(header)
    print(f"Wrote {args.out} ({args.alpha} bins, float Re/Im)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
